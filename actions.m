function A = actions(idxA)
% 离散行为编号到方向偏移的映射
% -------------------------------------------------------------------------
%   
%   函数 :
%   [A] = actions(idxA)
%   
%   输入 :
%   idxA  - 离散行为的编号
%   
%   输出 :
%   A     - 输出的行为对应的位置偏移

    
    ACTION = [   0,  -1;  % 左
                 -1,  0;  % 上
                 0,   1;  % 右
                1,   0;   % 下
                 0,   0]; % 保持静止
    A = ACTION(idxA,:);
    
end